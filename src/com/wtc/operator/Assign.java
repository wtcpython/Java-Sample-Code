package com.wtc.operator;

public class Assign {
    public static void main(String[] args) {
        // +=
        int a = 10;
        int b = 20;
        a += b;
        System.out.println(a);
        System.out.println(b);

        // -=
        int c = 20;
        int d = 10;
        c -= d;
        System.out.println(c);
        System.out.println(d);

        // *= /= %=
    }
}
