package com.wtc.loop;

public class ForLoop {
    public static void main(String[] args) {
        for (int i = 0; i < 5; i++) {
            System.out.println("Hello World!");
        }

        for (int i = 1; i <= 5; i++) {
            System.out.println(i);
        }

        for (int i = 5; i >= 1; i--) {
            System.out.println(i);
        }
    }
}
